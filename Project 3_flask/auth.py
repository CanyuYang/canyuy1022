def time_spend(func):
    @wraps(func)
    def _time_it(*args, **kwargs):
        start = start = datetime.now()
        try:
            return func(*args, **kwargs)
        finally:
            end_ = start = datetime.now() - start
            print(f"Total execution time:", end_ , "ms")
    return _time_it

